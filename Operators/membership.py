
""" ----------------------- in operator ----------------------------- """
'lo' in 'hello' # OUTPUT: True
'ol' in 'hello' # OUTPUT: False
2 in [1,2] # OUTPUT: True

""" ----------------------- in operator(case sensitive) ----------------------------- """
'hello' in 'hello' # OUTPUT: True
'Hello' in 'hello' # OUTPUT: False


""" ----------------------- not in operator ----------------------------- """
3 not in [1,2] # Output: True
'o' not in 'hello' # OUTPUT: False
